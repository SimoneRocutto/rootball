    <h3 class="slided-out transition-transform" id="sec-8-sub-1">Health</h3>
    <p class="slided-out transition-transform" id="sec-8-par-1">Since Rootball limits the player's movements, it can even be practiced by people who should avoid more intense sports (e.g. due to injuries).</p>
    <h3 class="slided-out transition-transform" id="sec-8-sub-2">Play anywhere</h3>
    <p class="slided-out transition-transform" id="sec-8-par-2">Rootball only needs a ball and two players. It doesn't need any net or special field.</p>
